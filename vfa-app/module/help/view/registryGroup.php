<div class="panel panel-default panel-root">
	<div class="panel-heading">
		<h3 class="panel-title">Inscription d'un groupe</h3>
	</div>
	<div class="panel-body">
		<p>L'inscription d'un groupe (CE, association, ...) à un prix s'effectue en 2 étapes</p>

		<div class="panel panel-default">
			<div class="panel-heading">
				<h4 class="panel-title text-info"><kbd>Etape 1</kbd> L'affectation du prix au
					groupe</h4>
			</div>
			<div class="panel-body panel-condensed">
				<p>A chaque début de prix, il faut associer le prix qui démarre aux groupes participants.<br>Pour chaque groupe
					qui s'inscrit :</p>
				<ol>
					<li>Listez les groupes : menu <kbd>Administrer / Groupes</kbd></li>
					<li>Si le groupe n'existe pas dans la liste, créez le groupe : bouton <kbd>Créer</kbd><br>
						Sinon sélectionnez un groupe puis modifiez-le : bouton <kbd>Modifier</kbd></li>
					<li>Dans le formulaire, affectez le prix au groupe en sélectionnant un prix dans le champ <kbd>Inscription au
							prix</kbd></li>
				</ol>
			</div>
		</div>
		<div class="panel panel-default">
			<div class="panel-heading">
				<h4 class="panel-title text-info"><kbd>Etape 2</kbd> L'inscription du correspondant du groupe</h4>
			</div>
			<div class="panel-body panel-condensed">
				<p>Il faut permettre au responsable du groupe, le correspondant qui gère le prix dans son groupe, d'ouvrir un
					compte sur le site avec un code d'inscription. Ce code lui permettra de l'affecter dans le bon groupe, de
					l'inscrire au prix et surtout de lui donner les droits pour inscrire les lecteurs de son groupe.
					Accessoirement, il pourra aussi voter.<br>Attention, ce code d'inscription n'est valable qu'une seule fois, il
					est supprimé dès qu'il est utilisé.</p>

				<p>La génération du code d'inscription du correspondant ou permission d'inscription</p>
				<ol>
					<li>Accédez au formulaire pour créer une permission d'inscription pour un correspondant : menu <kbd>Inscriptions
							/ Inscription d'un
							correspondant / Créer une permission</kbd>
						<ul>
							<li>Sélectionnez le groupe : champ <kbd>Affectation au groupe</kbd></li>
							<li>Modifiez la date max si nécessaire : champ <kbd>Date maximale d'inscription</kbd></li>
							<li>Une fois validé, vous obtenez le code d'inscription pour le correpondant dans l'écran suivant</li>
						</ul>
					</li>

					<li>Envoyez le code au correspondant en le recopiant dans un mail.<br>Un exemple est fourni en cliquant sur
						le titre de la boite <kbd>Exemple de mail à envoyer au correspondant</kbd></li>
				</ol>
			</div>
		</div>
		<div class="panel panel-default">
			<div class="panel-heading">
				<h4 class="panel-title text-info">Gestion des permissions</h4>
			</div>
			<div class="panel-body panel-condensed">
				<p>Actions possibles pour gérer les inscriptions des correspondants</p>
				<ul>
					<li>Visualisation de la liste des permissions d'inscription en cours pour les correspondants : menu <kbd>Inscriptions
							/ Inscription d'un
							correspondant / Voir les permissions en cours</kbd> <br>
						Cette liste contient toutes les permissions non encore utilisées par les correspondants
					</li>
					<li>Visualisation d'une permission : à partir de la liste des permissions, cliquez sur une permission pour voir son détail</li>
					<li>Modification d'une permission : à partir du détail d'une permission, bouton <kbd>Modifier</kbd></li>
					<li>Suppression d'une permission : à partir du détail d'une permission, bouton <kbd>Supprimer</kbd></li>
				</ul>
			</div>
		</div>
	</div>
</div>
